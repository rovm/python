#세트 만들기
fruits = {'strawberry', 'grape', 'orange', 'pineapple', 'cherry'}
print(fruits)

fruits = {'orange', 'orange', 'cherry'}
print(fruits)

fruits = {'strawberry', 'grape', 'orange', 'pineapple', 'cherry'}
print('orange' in fruits)
print('peach' in fruits)
print('orange' not in fruits)
print('peach' not  in fruits)

#set를 사용하여 세트 만들기
a = set('apple')
print(a)

b = set(range(5))
print(b)

c = set()
print(c)

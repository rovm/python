#정수 계산하기
a = 1 + 1
print(a) #2

a = 1 - 2
print(a) # -1

a = 5 / 2
print(a) # 2.5

a = 4 / 2
print(a) # 2.0

a = 5.5 // 2
print(a) # 2

a = 4 // 2
print(a) # 2

a = 4.1 // 2.1
print(a) # 1.0

a = 5 % 2
print(a) # 1

a = 2 ** 10
print(a) # 1024

a = int(3.3)
print(a) # 3

a = int(5/2)
print(a) # 2

a = int('10')
print(a) # 10

a = type(10)
print(a) # <class 'int'>

a = divmod(5,2)
print(a) # (2,1)

a, b = divmod(5,2)
print(a)
print(b)
print(a, b)

a = 0b110
print(a) # 6

a = 0o10
print(a) # 8

a = 0xf
print(a) # 15
